extension UIColor {
     class func appColor() -> UIColor {
        return UIColor(red: 34.0/255, green: 135.0/255, blue: 163.0/255, alpha: 1.0)
    }
    
    class func sectionColor() -> UIColor {
        return UIColor(red: 239.0/255, green: 239.0/255, blue: 244.0/255, alpha: 1.0)
    }
}
